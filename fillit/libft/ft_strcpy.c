#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;
	char	*s;

	s = (char *)src;
	i = 0;
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
