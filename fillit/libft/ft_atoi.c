#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	d;
	int	sign;

	i = 0;
	d = 0;
	sign = 1;
	while (('\t' <= str[i] && str[i] <= '\r') || (str[i] == ' '))
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] != '\0' && ('0' <= str[i] && str[i] <= '9'))
	{
		d = d * 10 + (str[i] - '0');
		i++;
	}
	i++;
	return (sign * d);
}
